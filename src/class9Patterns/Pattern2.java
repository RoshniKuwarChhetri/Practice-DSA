package class9Patterns;

public class Pattern2 {
public static void main(String[] args) {
	int n=5;
	for(int i=1;i<=n;i++) {
		for(int j=1;j<=n;j++) {
		System.out.print(i +"" + ""+j);	//(i +"" + ""+j+" ")no need of secont line+
		System.out.print(" ");
		}
		System.out.println();
	}
}
}
















