package class10;

public class AlphabetPattern3 {
 public static void main(String[] args) {
	 char c='a';
	for(int i='a';i<='e';i++) {
		for(int j='a';j<='e';j++) {
			System.out.print(c + " ");
			c++;
		}
		System.out.println();
	}
}
}
